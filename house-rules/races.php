<H2>Races</H2>

<h3>The Problems</h3>
<ol>
<li>
In Dungeons and Dragons ones choice of race is quite important for
starting characters but these differences quickly fade into
insignificance after a few levels.
<p>This problem is never really addressed in any WOTC product, but in
Unearthed Arcana, the section on Racial Paragons provides an excellent
clue which can be the starting point for solving both problems, along 
with the racial progression ideas spelled out in the monster manuals.</p>
</li>

<li>Many more exotic races have level adjustment penalties for the
significant initial benefits they give. However, this can present
problems for adventures for low levels, and at high levels these races
become underpowered because of the level penalty.

<P>
Unearthed Arcana gives a couple of interesting ideas here: one is to
buy off the expensive level adjustments progressively during the
character's career. This works because of the self correcting nature of the DnD
XP system, however, it does not address the early levels, where while
the character may be tougher than others in the party, the player must
wait several levels before his character can improve or advance.</p>
</li>

<li>Certain races take attribute penalties (particularly charisma) to
reflect an attitude rather then an actual lack of the attribute. This
has unfortunate consequences in certain skills which the race aught to
be good at, for example, for dwarves, use magical device, or intimdate
for half-orcs.  </p>
</li>

</ol>

<h3>The Solution</h3>

<P> For problem (1), I propose to address the
growing irrelevance of race as one advances in level by having various
racial benefits kick in as the character goes up in level. To address
point (2), slightly boost all 0 LA races, to allow +1 LA races to
start at par. And finally, for races penalized in charisma, I impose
penalties on specific skills instead.</p>

