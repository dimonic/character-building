  <h4>Elves</h4>

    <p>Elves are effectively immortal: they do not age (in game terms)
    past middle age, and do not die of "natural causes". (In adventuring
    terms this is just flavour anyway).</p>

    <p>Elves gain an additional <b>+2 Wis</b>, but take a penalty of
    -2 to Diplomacy and gather information when dealing with other races. 
    Other than this, they are already good enough. The preferred class 
