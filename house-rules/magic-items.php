<h2>Magic Items</h2> 

<h3>How can we fight monster 'X' without magic?</h3>

  <p>You will not be without magic. Magic will simply not be
  superabundant. You will have access to powerful spells from the
  casters in your party. There are also a few extra mechanisms which
  will help fill some of the perceived magic item deficit, without
  returning to the bland arms race that 3e has become.</p>

<h4>Special Item Feats</h4> 

  <p>I have designed some <a href="../downloads/item_feats.pdf">Item
  Enhancement feats</a> that may be taken by any character.

<h4>Legendary Items.</h4>

  <p>We can use the legendary weapon system from Unearthed Arcana, and
  the ancestral weapon feats from Book of Exalted Deeds.</p>

<h4>Leveled Treasures</h4>

  <p>The <a href="leveled-treasure.php">Leveled Treasures</a> system
  by Steven Kenson published in Dragon 289 is also very promising.</p>

<h4>Item Creation</h4>

  <p>Characters will have full access to item creation feats. However,
  this is a low gold campaign, as well as low magic. Creation costs
  are not lowered, and XP costs are not waived. Thus, items will have
  to be carefully considered to ensure they will justify the
  costs. The DM will also require that all prerequisites for item
  creation, including laboratory, forge, materials, craft skills
  etc. are met before any item can be made, and may additionally
  require special material components, or quests in order to complete
  the process.</p>

