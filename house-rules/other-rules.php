<h3><a name="saves">Modified Saves</a></h3>

  <p>The DM may at his discretion use different abilities for some
  saves. Some examples follow:</p>

  <p><b>Reflex (Int):</b> Quick thinking</p>
  <p><b>Reflex (Cha):</b> Stopping an angry, charging foe</p>
  <p><b>Reflex (Str):</b> Grabbing a falling friend</p>
  <p><b>Reflex (Wis):</b> Stopping yourself before you do something foolish</p>

  <p><b>Will (Con):</b> Ignoring physical distractions</p>
  <p><b>Will (Int):</b> Enduring something intellectually challenging</p>
  <p><b>Will (Cha):</b> Resisting something that attacks your own identity (like a dominate person effect)</p>

  <p><b>Fort (Str):</b> Resisting something attempting to knock you down or hold you in place</p>
  <p><b>Fort (Int):</b> Resisting something that is physically affecting your brain</p>

  <p>The full text of this idea is from <a
  href="http://www.montecook.com/arch_dmonly31.html">Monte Cook's</a>
  site.</p>

