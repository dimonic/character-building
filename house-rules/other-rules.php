<h2>Other House Rules</h2>

<h3><a name="action">Action points</a></h3>
  <p>It is never fun when a character dies through sheer bad luck
  when doin the right thing. Even DMs don't want this to happen.
  Action points provide a mechanism to <em>occasionally</em> shift
  the odds in your favour.</p>

  <p>At each level, the character gains a pool of action points
  equal to (level / 2) + 5.  These can be used as needed, they 
  are not kept over when you level up. They can be used for various 
  game effects. Here are some examples:</p>

  <ul>
    <li>Improve a die roll for an attack, ability check, skill check,
    level check or a saving throw.

    <p>Roll a d6<sup>*</sup> and add it to your roll. This must be
    done after the die roll, but before the result is determined.</p></li>

    <li>To automatically stabilize a dying character.</li>

  </ul>

  <p><sup>*</sup> at 8th - 14th level, roll 2d6, from 15th - 20th, 3d6.</p>

<h3><a name="languages">Languages</a></h3>

  <p>Languages will in general be resolved using the standard
  rules. However, understanding, or relating a difficult or unusual
  concept will carry a risk of misunderstanding. Normal conversation
  has a DC of 10 and characters can always "take 10" (note that low
  intelligence characters can still misunderstand things). An unusual
  situation, or a tricky concept might require a DC 15 speak language
  check.</p>

  <p>Understanding a non-native tongue is even more dificult. the
  standard DC is 12, and more difficult concepts will require a DC 17
  speak language check.</p>

  <p>Only the character's regional language is considered "native" in
  these rules. Common for example, is considered a second language if
  common is not the character's regional language.</p>

<h3><a name="literacy">Literacy</a></h3>

  <p>We have decided (in the interests of a more gritty feel) to
  reduce literacy in our game world. Only clerics, wizards, monks and
  bards are presumed to be literate. All other classes will have to
  "purchase" literacy in each language they speak* using skill
  points. See each <a href="classes/">class</a> for more detail.</p> 

  <p>Literacy will not be an all or nothing deal either: to understand
  more complex texts, or dificult concepts, characters will need to
  make intelligence checks, or fail to understand, or even misread the
  documents.</p>

  <p>*If the campaign has alphabets that are used for more than one
  language, then literacy will apply to all languages the character
  knows that share the same alphabet.</p>

