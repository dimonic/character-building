<h2>Highest-Level Locals</h2>

   <p>All PC classes are less common than the DMG
   indicates. Spellcasters will be rarer still. The following table
   replaces the <b>"Highest level locals"</b> table in the DMG page 139.</p>

   <p>Of course this table represents the good or non-aligned locals
   that exist as background to your campaign. Those forces aligned
   against the characters can be significantly strong and diverse as
   desired.</p>

<table>
  <tr><th class="left">Class</th><th class="left">Character level</th></tr>

  <tr><td class="left">Adept</td>      <td class="left">1 + community modifier</td></tr>
  <tr><td class="left">Aristocrat</td> <td class="left">1d4 + community modifier</td></tr>
  <tr><td class="left">Barbarian</td>  <td class="left">1d3 - community modifier + 1</td></tr>
  <tr><td class="left">Bard</td>       <td class="left">community modifier</td></tr>
  <tr><td class="left">Cleric</td>     <td class="left">community modifier</td></tr>
  <tr><td class="left">Commoner</td>   <td class="left">4d4 + community modifier</td></tr>
  <tr><td class="left">Druid</td>      <td class="left">1d2 - community modifier + 1</td></tr>
  <tr><td class="left">Expert</td>     <td class="left">3d4 + community modifier</td></tr>
  <tr><td class="left">Fighter</td>    <td class="left">1d2 + community modifier</td></tr>
  <tr><td class="left">Monk</td>       <td class="left">community modifier</td></tr>
  <tr><td class="left">Paladin</td>    <td class="left">community modifier</td></tr>
  <tr><td class="left">Ranger</td>     <td class="left">1d4 - community modifier + 1</td></tr>
  <tr><td class="left">Rogue</td>      <td class="left">1d8 + community modifier</td></tr>
  <tr><td class="left">Sorcerer</td>   <td class="left">community modifier - 2</td></tr>
  <tr><td class="left">Warrior</td>    <td class="left">2d4 + community modifier</td></tr>
  <tr><td class="left">Wizard</td>     <td class="left">community modifier - 2</td></tr>
</table>

  <p>At the DM's discretion, groups of smaller communities can be
  considered as amalgamated, and each amalgamation can be considered
  as a higher population centre for determining the highest level
  local for the entire area. For example, a cluster of 4-5 Thorps can
  be considered as a hamlet for purposes of determining the local
  class populations.</p>

  <p>See also S. John Ross's excellent <a
  href="http://www.io.com/~sjohn/demog.htm">demography article</a>.</p>

