<h2>Leveled Treasures</h2> 

  <p>Items start as as low powered items, but grow in power as the
  owner gains experience, and perhaps in response to fulfillment of
  certain quests.</p>

<h3>Market price</h3>

  <p>Since leveled items do not "come into" all their powers right away,
  and they require some of their owners' life essence to activate, they
  cost less to make than regular magic items.</p>

  <p><b>Weapon:</b> Equivalent enhancement bonus squared x 1,200 gp</p>

  <p><b>Armour:</b> Equivalent enhancement bonus squared x 600 gp</p>

  <p>or in both cases, normal price x .6.</p>

