<h2>Classes</h2>

  <h3><a href="spellcasters.php" title="Spell progression tables for all casters">Major spellcasters</a></h3>

    <p>All spellcasters are rare. Magic is rare. Spellcaster's
    progressions have been <a href="spellcasters.php" 
    title="Spell progression tables for all casters">significantly
    altered</a>, giving them slower spell progressions, allowing a
    maximum of 7th level spells by 19th level. Since this moves the
    acquisition of 5th level spells from 9th up to 13th level
    (including such spells as raise dead and teleport), this should
    extend that portion of the game where quasi mediaeval styles rule
    the day.</p>

  <h3>Other classes</h3>

    <p>I feel that the cleric, wizard, druid and (to a lesser extent)
    sorcerer are the most powerful classes in the standard game. It
    remains to be seen whether the adjustments I am proposing will
    result in a balanced, or fun game. I do not feel that rangers or
    paladins present any issues with regard to balancing or low/high
    magic issues.  If neccesary, I remain prepared to scale back some
    of the other classes abilties, such as barbarian rage. I suspect
    that it will not be neccesary however.</p>

  <h3>Prestige Classes</h3>

    <p>I have introduced a <a
    href=prestige-classes.php#martial-training">Martial Training</a>
    3-level prestige class to allow non-martial character classes to
    gain access to other weapons, and more feats without multiclassing
    penalties, and the <a
    href="prestige-classes.php#occult-student">Student of the
    Occult</a> for non-spellcasters wishing to dabble in magic.</p>

  <h3>Weapon groups</h3> 

    <p>We use the Unearthed Arcana variant "weapon groups" rules for
    weapon proficiencies. Other weapon feats may be applied to weapon
    groups as a whole, instead of to individual weapons. This includes
    weapon focus, improved critical and specialization. This is to
    give flexibility that will not only make up for the "wasted feat"
    syndrome when you find a new magical weapon, but will also
    encourage distinctive flavour for fighting types.</p>

  <h3><a name="barbarian">Barbarian</a></h3>

    <p>Weapon Group (Basic weapons) plus any other three.</p>

    <p>Literacy: the barbarian is not only illiterate, he is
    unfamiliar with the concept of written symbols. He must use 3
    skill points to acquire literacy in the first language he learns
    to read and write, and it is a cross class skill from then on.</p>

  <h3><a href="bard.php" title="Details of Bard class">Bard</a></h3>

    <p>Can learn a new spell in place of one he already knows <i>at every level he 
    gains access to a new spell level</i></p>

    <p>Weapon group (Basic Weapons) plus any other two.</p>

    <p>The bard is automatically literate in each langauge that he knows.</p>

