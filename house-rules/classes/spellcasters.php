<h2>Spell Casters</h2>

    <p>I decided to push at least spell-levels 8 and 9 off into epic
    levels, and have a maximum of 7th level available to standard
    (20th) level casters, tapering spell progression to achieve this
    result, and then slowing the bard down a little to keep him
    behind.</p>

    <p>To sweeten the pot slightly (thanks, again Andy), I have given
    the major spellcasters a few extra feats to keep certain levels
    from being total non-events, and to make up for reduced options
    for prestige classes.</p>

    <p>Following is a table showing the levels of spells the different
    spell casters acquire at each class level.</p>

