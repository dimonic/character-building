    <h5>Class features:</h5>

    <p><b>Weapon and armor proficiency:</b> the martially trained are
    proficient with one additional weapon group, light and medium
    armour, and shields.</p>

    <p><b>Bonus fighter feat:</b> at each level, the martially trained
    may select any feat marked as "fighter", provided that they meet
    the prerequisites for the feat.</p>
    

  <h3><a name="occult-student">Student of the Occult</a></h3>

    <p>This prestige class represents repeated exposure to, and
    developed interest in matters of arcane or divine study and
    lore. The Student must choose when taking this class whether to
    specialize in arcane or divine lore. The arcance student may only
    use arcane incantations, the divine may only cast divine
    incantations.</br> 
    <b>Hit Die:</b> d4.</p>

    <h5>Requirements:</h5>

    <p>Must <em>not</em> already by a spell caster of the type
    selected in the class. For example, you may "dabble' in the divine
    if you are an arcane caster, but not if you are already a divine
    caster.<br>
    <b>Skills:</b> Literacy, Knowledge (arcana) 4 ranks for the student of
    arcane, <em>or</em> Knowledge (religion) 4 ranks for the
    student of the divine.</p>

    <h5>Class skills:</h5>

    <p>The Occult Student's class skills are: concentration, craft,
    heal, knowledge (all skills taken separately), profession, 
    spellcraft and use magic device.<br>
    <b>Skill points at each level:</b> 4 + int modifier.</p>

