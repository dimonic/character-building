  <h3><a name="martial-training">Martial training</a></h3>

    <p>This prestige class represents taking a training course for a
    militia, or a brief stint in a mercenary corps, or some other such
    exposure to military training.<br>
    <b>Hit Die:</b> d8.</p>

    <h5>Requirements:</h5>

    <p>Must serve at least two weeks with exposure to military
    training (either personal coach, or military organization).<br>
    <b>Base attack bonus:</b> +1.</p>

    <h5>Class skills:</h5> 

    <p>The martially trained's class skills are climb, craft, handle
    animal, intimidate, jump, ride and swim.<br> <b>Skill points at
    each level:</b> 2 + int modifier.<p>

