    <h5>Class features</h5>

    <p><b>Weapon and armor proficiency:</b> the Student of the Occult
    gains no additional armour or weapons proficiencies.</p>

     <p><b>Spellcrafter:</b> the Student of the Occult may add
    their levels in Student of the Occult as a bonus to their
    spellcraft check, and spellcraft becomes a class skill for that
    character, whichever class they advance in.<p>

    <p><b>Read magic:</b> the Student of the Occult may <i>read
    magic</i> once per day per level of Student of the Occult. This is
    a spell-like ability.</p>

    <p><b>Transcribe:</b> starting at 2nd level, the Student of the
    Occult can accurately transcribe magical documents of the type
    selected (arcane or divine).</p>

    <p><b>Incantations:</b> starting at 2nd level, the Student of the
    Occult can cast spells from magical texts, requiring complex
    rituals, and taking 10 minutes per spell level to cast.</p>

    <p><b><a name="fetish">Occult fetish:</a></b> at 3rd level, the
    Student of the Occult can create a special type of magic item
    known as a fetish. A fetish takes up a body location "slot"
    according to the design of the creator. For example, a fetish may
    be made as a necklace, headgear, or a ring, or any other location
    as desired.  The fetish is a single spell storing item of maximum
    level 3, which activates when the condition appropriate to its use
    occurs, for example: a fetish for Bull's Strength would activate
    when the character begins exerting themselves strenuously, such as
    in combat, or lifting a heavy load. A fetish for Cure Light Wounds
    would activate when the wearer took damage, or when the wearer
    drops below 0 hit points (creator's option). The spell has all
    normal effects as if cast by the creator once activated, including
    duration. Treat the fetish as a potion for gold and experience
    point cost. The Student must find an appropriate incantation for
    the spell they wish to use.</p>

