<h2>Economics</h2>

  <p>The world has far less "cash" than the default D&D world. Liquid
  cash is rare, and only accumulated by the very wealthy. Change all
  amounts on page 137 (table 5&mdash;2) to <em>silver pieces</em>.</p>

  <p>Some equipment prices are <a
  href="equipment.php">adjusted</a>. Most are unchanged. Specialised
  PC equipment, such as magical components, thieves tools, weapons and
  armour, trained mounts, may be more expensive. Food and drink served
  in eating establishments is priced about right.</p>

  <p>The world is not really "poorer" than in the D&D default, there
  is just not that amount of liquid cash around. Wealth is primarily
  held in land and commodities. Farmers and landlords alike trade
  primarily in such things as cattle, bushels of wheat, barrels of
  salted port, salt itself, cords of lumber, arable or hunting land
  and labour.</p>

  <p>However, the new silver piece values <em>do</em> represent the
  most expensive magical items or adventurers equipment that a
  particular locale can supply. We feel that although a village of 200
  souls can provide 100 gp worth of wheat or cattle, such a small
  community cannot realistically be expected to stock and sell a chain
  shirt, composite longbow, or a magical potion on the open market.
  Such items would (under these rules) only be found in Town sized or
  larger settlements.</p>

  <p><b>NOTE:</b> since overall campaign wealth is reduced, and we
  expect the accumulation of magic items to be also significantly
  reduced, we have reduced the benefits confered by <a
  href="other-rules.php#vow-of-poverty">vow of poverty</a> from the
  Book of Exalted Deeds.</p>

  <h3><a name="financial-drawbacks">Financial Drawbacks</a></h3>

  <p>We have introduced some characteristics which will allow
  characters to vary their wealth levels. These are similar to flaws
  as found in Unearthed Arcana.</p>

  <h4>Destitute</h4>

  <p>The character has no lodgings, no means of support, no equipment,
  and only the barest garments.</p>

  <p><b>Pre-requisistes:</b> Must be first level. May not be of the
  nobility (aristocrat), or a paladin. This feat may also
  not be taken along with a vow of poverty.</p>

  <p><b>Benefits:</b> The character gains two extra feats at first
  level. This character may stay at poor inns, and subsist on poor
  food without ill effect. This feat grants +2 bonus on gather
  information, bluff and diplomacy amongst the lower class, -4 penalty
  amongst middle and upper class.</p>

  <p><b>Special:</b> a ranger or barbarian taking this drawback only
  gain one bonus feat.</p>

  <h4>Poor</h4>

  <p>The character has less wealth than normal, and appears somewhat
  shabby.</p>

  <p><b>Pre-requisistes:</b> must be first level. May not be taken in
  conjunction with vow of poverty.</p>

  <p><b>Benefits:</b> The character gains one extra feat. This
  character may stay at poor inns, and subsist on poor food without
  ill effect. This feat causes -2 penalty to bluff, diplomacy and
  gather information amongst the middle and upper classes.</p>

  <p><b>Special:</b> Rangers and barbarians do not get the bonus feat
  if they take this drawback.</p>


  <h3><a name="wealth-feats">Wealth feats</a></h3>

  <h4>Well-to-do</h4>

  <p>The character hails from a financially well-off background.</p>

  <p><b>Pre-requisites:</b> must be first level.</p>

  <p><b>Benefits:</b> the character starts off with double starting
  money.</p>

  <p><b>Special:</b> This character may only stay at good inns, and
  eat good food or better. He will desire the fancy versions of
  whatever equipment he buys, and wear more ostentatious clothes. If
  forced to live beneath these levels, he will incur a -1 morale
  penalty to attacks, saves and skill checks. This character will
  suffer a -2 circumstance penalty on diplomacy checks with those of
  the lower classes, and a +2 circumstance bonus to diplomacy amongst
  the middle classes.</p>

  <h4>Wealthy</h4>

  <p>The character has come into a large sum of money.</p>

  <p><b>Pre-requisites:</b> aristocrat level 1, or have the
  "mercantile background" feat.</p>

  <p><b>Benefits:</b> character's starting wealth is in gold
  pieces. If the character takes this feat at higher level, he
  receives a windfall inheritance of half the "character wealth by
  level" chart in gold pieces.</p>

  <p><b>Special:</b> This character may only stay at good inns, and
  eat good food or better. He will desire the fancy versions of
  whatever equipment he buys, and wear more ostentatious
  clothes. Living at lower expense levels he will incur a -2 morale
  penalty to attacks, saves and skill checks. This character will
  incur a -2 circumstance modifier to diplomacy checks made with thos
  of the lower classes, and a +2 circumstance modifier diplomacy
  checks with the middle classes.</p>

