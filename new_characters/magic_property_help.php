  <p>The format for the 'properties' field for magic items that grant
  bonuses is:

    <blockquote><b>
      attribute=bonus(type)
    </b></blockquote>

  where attribute could be such as 'Str', or 'Initiative', or
  'AC', or 'HP', and bonus could be '+3', or '-1', and type could
  be 'natural armor', or 'circumstance' for example. multiple
  bonuses can be strung together with the '&' sign, so an item that
  grants alertness can be coded as:

  <blockquote><b>
    Spot=+2&listen=+2
  </b></blockquote>

  and an amulet of natural armor is coded as:
  <blockquote><b>
    AC=+1(natural armor)
  </b></blockquote>
  </p>

